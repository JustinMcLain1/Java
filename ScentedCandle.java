package Assign5;

public class ScentedCandle extends Candle{
	String scent;
	double price;
	
	public String getScent() {
		return scent;
	}
	
	public void setScent(String scent) {
		this.scent = scent;
	}
	
	public int getHeight() {
		return height;
	}
	
	public double getPrice() {
		return price = getHeight()*3;
	}

}
