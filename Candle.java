package Assign5;

public class Candle {
	
	String color;
	int height;
	double price;
	
	public double getPrice() {
		return price = getHeight() * 2;
	}
	
	public void setColor(String colors) {
		color = colors;
	}
	
	public void setHeight(int hght) {
		height = hght;
	}
	
	public String getColor() {
		return color;
	}
	
	public int getHeight() {
		return height;
	}

}
